#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	b = 1;
	a = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\v' \
			|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		b = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		a = (a * 10 + (str[i] - 48));
		i++;
	}
	return (a * b);
}
