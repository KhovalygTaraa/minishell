#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	a;

	a = 0;
	if (s)
	{
		while (s[a] != 0)
		{
			ft_putchar_fd(s[a], fd);
			a++;
		}
	}
}
